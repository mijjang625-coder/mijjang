/**
 * 카테고리 가이드를 AI 프롬프트용 텍스트 블록으로 변환
 * @param {string} productType - 사용자가 선택한 카테고리
 * @returns {string} - 프롬프트에 첨부할 가이드 텍스트 (없으면 빈 문자열)
 */
export function buildCategoryGuideBlock(productType) {
  if (!productType) return '';
  const guide = CATEGORY_GUIDES[productType];
  if (!guide) return '';

  const lines = [];
  lines.push('');
  lines.push(`[🎯 카테고리별 작성 가이드 — "${productType}"]`);
  lines.push(`이 제품은 "${productType}" 카테고리입니다. 아래 가이드를 반드시 따라주세요:`);
  lines.push('');
  lines.push(`▸ 톤: ${guide.tone}`);
  lines.push('');
  lines.push(`▸ 우선 강조 포인트 (P1~P10 전반에서 자주 등장시킬 것):`);
  guide.focusPoints.forEach((p, i) => lines.push(`  ${i + 1}. ${p}`));
  lines.push('');
  if (guide.avoidWords?.length > 0) {
    lines.push(`▸ 피해야 할 단어 (어울리지 않음): ${guide.avoidWords.join(', ')}`);
    lines.push('');
  }
  lines.push(`▸ 사진 우선순위: ${guide.photoEmphasis}`);
  lines.push('');
  lines.push(`▸ P1 헤드라인 톤: ${guide.p1Style}`);
  lines.push(`▸ P2 효익 카드 톤: ${guide.p2Style}`);
  lines.push('');
  if (guide.examples?.length > 0) {
    lines.push(`▸ 참고 예시 (그대로 베끼지 말고 톤만 참고):`);
    guide.examples.forEach((ex) => lines.push(`  · ${ex}`));
  }
  lines.push('');
  lines.push(`⚠️ 위 가이드는 시스템 프롬프트의 P1~P10 구조와 충돌하지 않습니다.`);
  lines.push(`   구조는 그대로 유지하되, 카피의 톤/강조점/사진 추천만 카테고리에 맞게 조정하세요.`);
  return lines.join('\n');
}
